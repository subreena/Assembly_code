;DOT MATRIX LINE
CODE SEGMENT
    ASSUME CS:CODE,DS:CODE,ES:CODE,SS:CODE
    
    PPIC_C EQU 1EH
    PPIC  EQU 1CH
    PPIB EQU 1AH
    PPIA EQU 18H
    
    ORG 1000H
    
    ;PASSING CONTROL WORD TO CONTROL REGISTER
    MOV AL,80H
    OUT PPIC_C,AL
    
    ;GREEN LIGHT OFF
    MOV AL,11111111B
    OUT PPIA,AL
    
    ;ROW ON
    MOV AL,11111111B
    OUT PPIC,AL
    
    L1:
    MOV AL,11111110B
    
    L2:
    OUT PPIB,AL
    CALL DELAY
    ROL AL,1
    JC L2
    JMP L1   
 
    DELAY: MOV CX,0FFFFH
    TIMER:
    NOP
    NOP
    NOP
    NOP
    LOOP TIMER
    RET
    
    CODE ENDS
END